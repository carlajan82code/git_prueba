$(document).ready(function () {
  let i = 0;
  let countryList = getCountries();
  let countrySelector = document.getElementById("selectorPais");

  countryList.forEach(function (country) {
    let optionCreated = document.createElement("option");
    if (i == 0) {
      optionCreated.value = "";
      optionCreated.text = country;
      optionCreated.disabled = true;
      optionCreated.selected = true;
    } else {
      optionCreated.value = country;
      optionCreated.text = country;
    }
    countrySelector.appendChild(optionCreated);
    i++;
  });

  i = 0;
  let yearList = getYears();
  let yearSelector = document.getElementById("anio_nacimiento");
  yearList.forEach(function (year) {
    let optionCreated = document.createElement("option");
    if (i == 0) {
      optionCreated.value = "";
      optionCreated.text = year;
      optionCreated.disabled = true;
      optionCreated.selected = true;
    } else {
      optionCreated.value = year;
      optionCreated.text = year;
    }
    yearSelector.appendChild(optionCreated);
    i++;
  });
});

