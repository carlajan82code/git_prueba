function getYears() {
  let years = [];
  let currentYear = new Date().getFullYear();

  for (var year = 1920; year <= currentYear; year++) {
    if (year == 1920) { years.push("Seleccione su año de nacimiento") }
    years.push(year.toString());
  }
  return years;
}